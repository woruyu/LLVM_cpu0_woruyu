namespace llvm {
class TargetMachine;

class woruyuInstPrinter : public MCInstPrinter {
public:
  woruyuInstPrinter(MCAsmInfo const &MAI, MCInstrInfo const &MII,
                 MCRegisterInfo const &MRI)
      : MCInstPrinter(MAI, MII, MRI) {}

  void printInst(MCInst const *MI, raw_ostream &O, StringRef Annot, MCSubtargetInfo const &STI) override;
  void printRegName(raw_ostream &OS, unsigned RegNo) const override;
  void printOperand(const MCInst *MI, unsigned OpNo, raw_ostream &O);
  void printMemOperand(const MCInst *MI, int opNum, raw_ostream &O);

  // Autogenerated by tblgen. defined in woruyuGenAsmWriter.inc
  void printInstruction(const MCInst *MI, raw_ostream &O);
  static const char *getRegisterName(unsigned RegNo);
  bool printAliasInstr(const MCInst *MI, raw_ostream &OS);

};

} // namespace llvm

